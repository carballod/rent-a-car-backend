class Car {
    constructor(id, brand, model, year, mileage, color, air_conditioning, passengers, transmission) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.mileage = mileage;
        this.color = color;
        this.air_conditioning = air_conditioning;
        this.passengers = passengers;
        this.transmission = transmission;
    }
}

module.exports = Car;